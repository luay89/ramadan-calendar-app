بَلْ بِفَضْلِكَ عَلَيْنا، لأنَّكَ أهْلُ التَّقْوى وَأهْلُ الْمَغْفِرَةِ، تُبْدِئُ بِالإحْسانِ نِعَماً، وَتَعْفُو عَنِ الذَّنْبِ كَرَماً

