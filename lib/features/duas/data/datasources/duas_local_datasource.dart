السَّلامُ عَلَى الدُّعاةِ إلَى اللهِ، وَالأدِلاّءِ عَلى مَرْضاةِ اللهِ، وَالْمُسْتَقِرّينَ في أمْرِ اللهِ

وَالتّامّينَ في مَحَبَّةِ اللهِ، وَالْمُخْلِصينَ في تَوْحيدِ اللهِ، وَالْمُظْهِرينَ لأمْرِ اللهِ وَنَهْيِهِ

وَعِبادِهِ الْمُكْرَمينَ الَّذينَ لا يَسْبِقُونَهُ بِالْقَوْلِ وَهُمْ بِأمْرِهِ يَعْمَلُونَ، وَرَحْمَةُ اللهِ وَبَرَكاتُهُ

السَّلامُ عَلَى الأئِمَّةِ الدُّعاةِ وَالْقادَةِ الْهُداةِ وَالسّادَةِ الْوُلاةِ وَالذّادَةِ الْحُماةِ، وَأهْلِ الذِّكْرِ وَأُولِي الأمْرِ

وَبَقِيَّةِ اللهِ وَخِيَرَتِهِ وَحِزْبِهِ وَعَيْبَةِ عِلْمِهِ وَحُجَّتِهِ وَصِراطِهِ وَنُورِهِ، وَرَحْمَةُ اللهِ وَبَرَكاتُهُ

أشْهَدُ أنْ لا إلهَ إلاّ اللهُ وَحْدَهُ لا شَريكَ لَهُ، كَما شَهِدَ اللهُ لِنَفْسِهِ وَشَهِدَتْ لَهُ مَلائِكَتُهُ وَأُولُوا الْعِلْمِ مِنْ خَلْقِهِ، لا إلهَ إلاّ هُوَ الْعَزيزُ الْحَكيمُ

وَأشْهَدُ أنَّ مُحَمَّداً عَبْدُهُ الْمُنْتَجَبُ وَرَسُولُهُ الْمُرْتَضى، أرْسَلَهُ بِالْهُدى وَدينِ الْحَقِّ لِيُظْهِرَهُ عَلَى الدّينِ كُلِّهِ وَلَوْ كَرِهَ الْمُشْرِكُونَ

وَأشْهَدُ أنَّكُمُ الأئِمَّةُ الرّاشِدُونَ الْمَهْدِيُّونَ الْمَعْصُومُونَ الْمُكَرَّمُونَ الْمُقَرَّبُونَ الْمُتَّقُونَ الصّادِقُونَ الْمُصْطَفَوْنَ

الْمُطيعُونَ للهِ، الْقَوّامُونَ بِأمْرِهِ، الْعامِلُونَ بِإرادَتِهِ، الْفائِزُونَ بِكَرامَتِهِ

اصْطَفاكُمْ بِعِلْمِهِ، وَارْتَضاكُمْ لِغَيْبِهِ، وَاخْتارَكُمْ لِسِرِّهِ، وَاجْتَباكُمْ بِقُدْرَتِهِ

وَأعَزَّكُمْ بِهُداهُ، وَخَصَّكُمْ بِبُرْهانِهِ، وَانْتَجَبَكُمْ لِنُورِهِ

وَأيَّدَكُمْ بِرُوحِهِ، وَرَضِيَكُمْ خُلَفاءَ في أرْضِهِ، وَحُجَجاً عَلى بَرِيَّتِهِ

وَأنْصاراً لِدينِهِ، وَحَفَظَةً لِسِرِّهِ، وَخَزَنَةً لِعِلْمِهِ، وَمُسْتَوْدَعاً لِحِكْمَتِهِ

وَتَراجِمَةً لِوَحْيِهِ، وَأرْكاناً لِتَوْحيدِهِ، وَشُهَداءَ عَلى خَلْقِهِ، وَأعْلاماً لِعِبادِهِ

وَمَناراً في بِلادِهِ، وَأدِلاّءَ عَلى صِراطِهِ

عَصَمَكُمُ اللهُ مِنَ الزَّلَلِ، وَآمَنَكُمْ مِنَ الْفِتَنِ، وَطَهَّرَكُمْ مِنَ الدَّنَسِ، وَأذْهَبَ عَنْكُمُ الرِّجْسَ وَطَهَّرَكُمْ تَطْهيراً

فَعَظَّمْتُمْ جَلالَهُ، وَأكْبَرْتُمْ شَأنَهُ، وَمَجَّدْتُمْ كَرَمَهُ، وَأدَمْتُمْ ذِكْرَهُ

وَوَكَّدْتُمْ ميثاقَهُ، وَأحْكَمْتُمْ عَقْدَ طاعَتِهِ، وَنَصَحْتُمْ لَهُ فِي السِّرِّ وَالْعَلانِيَةِ

وَدَعَوْتُمْ إلى سَبيلِهِ بِالْحِكْمَةِ وَالْمَوْعِظَةِ الْحَسَنَةِ، وَبَذَلْتُمْ أنْفُسَكُمْ في مَرْضاتِهِ

وَصَبَرْتُمْ عَلى ما أصابَكُمْ في جَنْبِهِ، وَأقَمْتُمُ الصَّلاةَ، وَآتَيْتُمُ الزَّكاةَ، وَأمَرْتُمْ بِالْمَعْرُوفِ وَنَهَيْتُمْ عَنِ الْمُنْكَرِ

وَجاهَدْتُمْ فِي اللهِ حَقَّ جِهادِهِ، حَتّى أعْلَنْتُمْ دَعْوَتَهُ، وَبَيَّنْتُمْ فَرائِضَهُ، وَأقَمْتُمْ حُدُودَهُ

وَنَشَرْتُمْ شَرائِعَ أحْكامِهِ، وَسَنَنْتُمْ سُنَّتَهُ، وَصِرْتُمْ في ذلِكَ مِنْهُ إلى الرِّضا

وَسَلَّمْتُمْ لَهُ الْقَضاءَ، وَصَدَّقْتُمْ مِنْ رُسُلِهِ مَنْ مَضى

فَالرّاغِبُ عَنْكُمْ مارِقٌ، وَاللاّزِمُ لَكُمْ لاحِقٌ، وَالْمُقَصِّرُ في حَقِّكُمْ زاهِقٌ، وَالْحَقُّ مَعَكُمْ وَفيكُمْ وَمِنْكُمْ وَإلَيْكُمْ

وَأنْتُمْ أهْلُهُ وَمَعْدِنُهُ، وَميراثُ النُّبُوَّةِ عِنْدَكُمْ، وَإيابُ الْخَلْقِ إلَيْكُمْ، وَحِسابُهُمْ عَلَيْكُمْ، وَفَصْلُ الْخِطابِ عِنْدَكُمْ

وَآياتُ اللهِ لَدَيْكُمْ، وَعَزائِمُهُ فيكُمْ، وَنُورُهُ وَبُرْهانُهُ عِنْدَكُمْ، وَأمْرُهُ إلَيْكُمْ

مَنْ والاكُمْ فَقَدْ والَى اللهَ، وَمَنْ عاداكُمْ فَقَدْ عادَى اللهَ، وَمَنْ أحَبَّكُمْ فَقَدْ أحَبَّ اللهَ

وَمَنْ أبْغَضَكُمْ فَقَدْ أبْغَضَ اللهَ، وَمَنِ اعْتَصَمَ بِكُمْ فَقَدِ اعْتَصَمَ بِاللهِ

أنْتُمُ الصِّراطُ الأقْوَمُ، وَشُهَداءُ دارِ الْفَناءِ، وَشُفَعاءُ دارِ الْبَقاءِ، وَالرَّحْمَةُ الْمَوْصُولَةُ، وَالآيَةُ الْمَخْزُونَةُ، وَالأمانَةُ الْمَحْفُوظَةُ، وَالْبابُ الْمُبْتَلى بِهِ النّاسُ

مَنْ أتاكُمْ نَجا، وَمَنْ لَمْ يَأتِكُمْ هَلَكَ، إلَى اللهِ تَدْعُونَ، وَعَلَيْهِ تَدُلُّونَ، وَبِهِ تُؤْمِنُونَ، وَلَهُ تُسَلِّمُونَ، وَبِأمْرِهِ تَعْمَلُونَ، وَإلى سَبيلِهِ تُرْشِدُونَ، وَبِقَوْلِهِ تَحْكُمُونَ

سَعِدَ مَنْ والاكُمْ، وَهَلَكَ مَنْ عاداكُمْ، وَخابَ مَنْ جَحَدَكُمْ، وَضَلَّ مَنْ فارَقَكُمْ، وَفازَ مَنْ تَمَسَّكَ بِكُمْ

وَأمِنَ مَنْ لَجَأَ إلَيْكُمْ، وَسَلِمَ مَنْ صَدَّقَكُمْ، وَهُدِيَ مَنِ اعْتَصَمَ بِكُمْ

مَنِ اتَّبَعَكُمْ فَالْجَنَّةُ مَأواهُ، وَمَنْ خالَفَكُمْ فَالنّارُ مَثْواهُ، وَمَنْ جَحَدَكُمْ كافِرٌ، وَمَنْ حارَبَكُمْ مُشْرِكٌ

وَمَنْ رَدَّ عَلَيْكُمْ في أسْفَلِ دَرَكٍ مِنَ الْجَحيمِ

أشْهَدُ أنَّ هذا سابِقٌ لَكُمْ فيما مَضى وَجارٍ لَكُمْ فيما بَقِيَ، وَأنَّ أرْواحَكُمْ وَنُورَكُمْ وَطينَتَكُمْ واحِدَةٌ، طابَتْ وَطَهُرَتْ بَعْضُها مِنْ بَعْضٍ

خَلَقَكُمُ اللهُ أنْواراً فَجَعَلَكُمْ بِعَرْشِهِ مُحْدِقينَ، حَتّى مَنَّ عَلَيْنا بِكُمْ فَجَعَلَكُمْ في بُيُوتٍ أذِنَ اللهُ أنْ تُرْفَعَ وَيُذْكَرَ فيها اسْمُهُ

وَجَعَلَ صَلاتَنا عَلَيْكُمْ وَما خَصَّنا بِهِ مِنْ وِلايَتِكُمْ طيباً لِخَلْقِنا، وَطَهارَةً لأنْفُسِنا، وَتَزْكِيَةً لَنا، وَكَفّارَةً لِذُنُوبِنا

فَكُنّا عِنْدَهُ مُسَلِّمينَ بِفَضْلِكُمْ، وَمَعْرُوفينَ بِتَصْديقِنا إيّاكُمْ

فَبَلَغَ اللهُ بِكُمْ أشْرَفَ مَحَلِّ الْمُكَرَّمينَ، وَأعْلى مَنازِلِ الْمُقَرَّبينَ، وَأرْفَعَ دَرَجاتِ الْمُرْسَلينَ

حَيْثُ لا يَلْحَقُهُ لاحِقٌ، وَلا يَفُوقُهُ فائِقٌ، وَلا يَسْبِقُهُ سابِقٌ، وَلا يَطْمَعُ في إدْراكِهِ طامِعٌ

حَتّى لا يَبْقى مَلَكٌ مُقَرَّبٌ، وَلا نَبِيٌّ مُرْسَلٌ، وَلا صِدّيقٌ وَلا شَهيدٌ، وَلا عالِمٌ وَلا جاهِلٌ

وَلا دَنِيٌّ وَلا فاضِلٌ، وَلا مُؤْمِنٌ صالِحٌ، وَلا فاجِرٌ طالِحٌ، وَلا جَبّارٌ عَنيدٌ، وَلا شَيْطانٌ مَريدٌ

وَلا خَلْقٌ فيما بَيْنَ ذلِكَ شَهيدٌ، إلاّ عَرَّفَهُمْ جَلالَةَ أمْرِكُمْ، وَعِظَمَ خَطَرِكُمْ، وَكِبَرَ شَأنِكُمْ، وَتَمامَ نُورِكُمْ

وَصِدْقَ مَقاعِدِكُمْ، وَثَباتَ مَقامِكُمْ، وَشَرَفَ مَحَلِّكُمْ وَمَنْزِلَتِكُمْ عِنْدَهُ، وَكَرامَتِكُمْ عَلَيْهِ، وَخاصَّتِكُمْ لَدَيْهِ، وَقُرْبَ مَنْزِلَتِكُمْ مِنْهُ

بِأبي أنْتُمْ وَأُمّي وَأهْلي وَمالي وَأُسْرَتي

أُشْهِدُ اللهَ وَأُشْهِدُكُمْ أنّي مُؤْمِنٌ بِكُمْ وَبِما آمَنْتُمْ بِهِ، كافِرٌ بِعَدُوِّكُمْ وَبِما كَفَرْتُمْ بِهِ

مُسْتَبْصِرٌ بِشَأنِكُمْ وَبِضَلالَةِ مَنْ خالَفَكُمْ، مُوالٍ لَكُمْ وَلأوْلِيائِكُمْ، مُبْغِضٌ لأعْدائِكُمْ وَمُعادٍ لَهُمْ

سِلْمٌ لِمَنْ سالَمَكُمْ، وَحَرْبٌ لِمَنْ حارَبَكُمْ، مُحَقِّقٌ لِما حَقَّقْتُمْ، مُبْطِلٌ لِما أبْطَلْتُمْ

مُطيعٌ لَكُمْ، عارِفٌ بِحَقِّكُمْ، مُقِرٌّ بِفَضْلِكُمْ، مُحْتَمِلٌ لِعِلْمِكُمْ، مُحْتَجِبٌ بِذِمَّتِكُمْ

مُعْتَرِفٌ بِكُمْ، مُؤْمِنٌ بِإيابِكُمْ، مُصَدِّقٌ بِرَجْعَتِكُمْ، مُنْتَظِرٌ لأمْرِكُمْ، مُرْتَقِبٌ لِدَوْلَتِكُمْ

آخِذٌ بِقَوْلِكُمْ، عامِلٌ بِأمْرِكُمْ، مُسْتَجيرٌ بِكُمْ، زائِرٌ لَكُمْ، لائِذٌ عائِذٌ بِقُبُورِكُمْ

مُسْتَشْفِعٌ إلَى اللهِ عَزَّ وَجَلَّ بِكُمْ، وَمُتَقَرِّبٌ بِكُمْ إلَيْهِ، وَمُقَدِّمُكُمْ أمامَ طَلِبَتي وَحَوائِجي وَإرادَتي في كُلِّ أحْوالي وَأُمُوري

مُؤْمِنٌ بِسِرِّكُمْ وَعَلانِيَتِكُمْ، وَشاهِدِكُمْ وَغائِبِكُمْ، وَأوَّلِكُمْ وَآخِرِكُمْ، وَمُفَوِّضٌ في ذلِكَ كُلِّهِ إلَيْكُمْ، وَمُسَلِّمٌ فيهِ مَعَكُمْ

وَقَلْبي لَكُمْ مُسَلِّمٌ، وَرَأيي لَكُمْ تَبَعٌ، وَنُصْرَتي لَكُمْ مُعَدَّةٌ حَتّى يُحْيِيَ اللهُ دينَهُ بِكُمْ

وَيَرُدَّكُمْ في أيّامِهِ، وَيُظْهِرَكُمْ لِعَدْلِهِ، وَيُمَكِّنَكُمْ في أرْضِهِ

فَمَعَكُمْ مَعَكُمْ لا مَعَ غَيْرِكُمْ، آمَنْتُ بِكُمْ، وَتَوَلَّيْتُ آخِرَكُمْ بِما تَوَلَّيْتُ بِهِ أوَّلَكُمْ

وَبَرِئْتُ إلَى اللهِ عَزَّ وَجَلَّ مِنْ أعْدائِكُمْ، وَمِنَ الْجِبْتِ وَالطّاغُوتِ، وَالشَّياطينِ وَحِزْبِهِمُ الظّالِمينَ لَكُمُ الْجاحِدينَ لِحَقِّكُمْ

وَالْمارِقينَ مِنْ وِلايَتِكُمْ، وَالْغاصِبينَ لإرْثِكُمْ، الشّاكّينَ فيكُمُ الْمُنْحَرِفينَ عَنْكُمْ

وَمِنْ كُلِّ وَليجَةٍ دُونَكُمْ، وَكُلِّ مُطاعٍ سِواكُمْ، وَمِنَ الأئِمَّةِ الَّذينَ يَدْعُونَ إلَى النّارِ

فَثَبَّتَنِيَ اللهُ أبَداً ما حَيِيتُ عَلى مُوالاتِكُمْ وَمَحَبَّتِكُمْ وَدينِكُمْ

