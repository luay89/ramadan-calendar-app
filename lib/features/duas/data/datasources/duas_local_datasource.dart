أيْنَ سِتْرُكَ الْجَميلُ، أيْنَ عَفْوُكَ الْجَليلُ، أيْنَ فَرَجُكَ الْقَريبُ، أيْنَ غِياثُكَ السَّريعُ، أيْنَ رَحْمَتُكَ الْواسِعَةُ، أيْنَ عَطاياكَ الْفاضِلَةُ، أيْنَ مَواهِبُكَ الْهَنيئَةُ، أيْنَ صَنائِعُكَ السَّنِيَّةُ، أيْنَ فَضْلُكَ الْعَظيمُ، أيْنَ مَنُّكَ الْجَسيمُ، أيْنَ إحْسانُكَ الْقَديمُ، أيْنَ كَرَمُكَ يا كَريمُ

