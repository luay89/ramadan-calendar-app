  /// الحصول على جميع الأدعية
  List<DuaEntity> getAllDuas() {
    return [
      ...getDailyDuas(),
      ...getWeeklyDuas(),
      ...getZiyarat(),
      ...getSpecialNightsDuas(),
      ...getSahifaSajjadiyaDuas(),
      ...getAfterPrayerDuas(),
      ...getQuranDuas(),
    ];
  }

  /// الحصول على الأدعية حسب التصنيف
  List<DuaEntity> getDuasByCategory(DuaCategory category) {
    return getAllDuas().where((dua) => dua.category == category).toList();
  }
